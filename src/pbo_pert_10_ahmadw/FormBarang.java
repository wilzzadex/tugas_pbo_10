    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if (txtKodeBarang.getText().equals("") || txtNamaBarang.getText().equals("") || txtJmlBarang.getText().equals("")
                || txtHargaBeli.getText().equals("") || txtHargaJual.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "LENGKAPI DATA !", "-->", JOptionPane.INFORMATION_MESSAGE);
        } else {
            String kbarang = txtKodeBarang.getText();
            String nbarang = txtNamaBarang.getText();
            String jbarang = txtJmlBarang.getText();
            String hbeli = txtHargaBeli.getText();
            String hjual = txtHargaJual.getText();
            try {
                long millis = System.currentTimeMillis();
                java.sql.Date date = new java.sql.Date(millis);
                System.out.println(date);
                String tgl = date.toString();
                Connection c = Koneksi.getKoneksi();
                String sql = "INSERT INTO tbl_barang VALUES (?, ?, ?, ?, ?, ?)";
                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, kbarang);
                p.setString(2, nbarang);
                p.setString(3, jbarang);
                p.setString(4, hbeli);
                p.setString(5, hjual);
                p.setString(6, tgl);
                p.executeUpdate();
                p.close();

            } catch (SQLException e) {
                System.out.println("Terjadi Error");
            } finally {
                loadData();
                kode();
                txtNamaBarang.setText("");
                txtJmlBarang.setText("");
                txtHargaBeli.setText("");
                txtHargaJual.setText("");
                JOptionPane.showMessageDialog(null, "Data berhasil tersimpan", "elekronik berkah", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

