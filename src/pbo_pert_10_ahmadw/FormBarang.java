    private void addTableClickListener() {
        tableInput.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int selectedRow = tableInput.getSelectedRow();
                if (selectedRow >= 0) {
                    txtKodeBarang.setText(model.getValueAt(selectedRow, 0).toString());
                    txtNamaBarang.setText(model.getValueAt(selectedRow, 1).toString());
                    txtJmlBarang.setText(model.getValueAt(selectedRow, 2).toString());
                    txtHargaBeli.setText(model.getValueAt(selectedRow, 3).toString());
                    txtHargaJual.setText(model.getValueAt(selectedRow, 4).toString());

                    btnSimpan.setEnabled(false);
                    btnHapus.setEnabled(true);
                    btnEdit.setEnabled(true);
                } else {
                    btnSimpan.setEnabled(true);
                    btnHapus.setEnabled(false);
                    btnEdit.setEnabled(false);
                }
            }
        });
    }

    public final void loadData() {
        btnSimpan.setEnabled(true);
        btnHapus.setEnabled(false);
        btnEdit.setEnabled(false);
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        try {
            Connection c = Koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM tbl_barang";
            ResultSet r = s.executeQuery(sql);
            while (r.next()) {
                Object[] o = new Object[5];
                o[0] = r.getString("kd_barang");
                o[1] = r.getString("nama_barang");
                o[2] = r.getString("jumlah_barang");
                o[3] = r.getString("harga_beli");
                o[4] = r.getString("harga_jual");
                model.addRow(o);
            }
            r.close();
            s.close();
        } catch (SQLException e) {
            System.out.println(e.toString());
        }
    }

    private void kode() {
        try {
            Connection c = Koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM tbl_barang ORDER by kd_barang desc";
            ResultSet r = s.executeQuery(sql);
            if (r.next()) {
                String nofak = r.getString("kd_barang").substring(1);
                String AN = "" + (Integer.parseInt(nofak) + 1);
                String Nol = "";
                switch (AN.length()) {
                    case 1:
                        Nol = "000";
                        break;
                    case 2:
                        Nol = "00";
                        break;
                    case 3:
                        Nol = "0";
                        break;
                    case 4:
                        Nol = "";
                        break;
                    default:
                        break;
                }
                txtKodeBarang.setText("B" + Nol + AN);
            } else {
                txtKodeBarang.setText("B0001");
            }
        } catch (SQLException e) {
            System.out.println(e.toString());
        }
    }

